public class BitOperate {
  public static void main(String[] args) {
    int i = 8, j = 4;
    System.out.println(i & j);
    System.out.println(i | j);
    System.out.println(1 << 2);
    System.out.println(4 >> 2);
    System.out.println(2 << 3);
  }
}
